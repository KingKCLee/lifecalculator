function esc(s){
  return String(s||'').replace(/[&<>"']/g, c => ({'&':'&amp;','<':'&lt;','>':'&gt;','"':'&quot;',"'":'&#39;'}[c]));
}

function relLink(term, allSlugs){
  const slug = allSlugs[term];
  if(slug) return `<a href="/terms/${slug}.html">${esc(term)}</a>`;
  return `<span class="rel-missing">${esc(term)}</span>`;
}


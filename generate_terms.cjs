function main(){
  // 기존 terms.json 로드
  const termsJson = JSON.parse(fs.readFileSync(path.join(TERMS_DIR, 'terms.json'), 'utf8'));
  const existing = termsJson.terms;

  // 기존 slug 맵 (파일명 역추적)
  const slugMap = {};
  const htmlFiles = fs.readdirSync(TERMS_DIR).filter(f => f.endsWith('.html') && f !== 'index.html' && f !== 'search.html');
  for(const f of htmlFiles){
    const t = fs.readFileSync(path.join(TERMS_DIR, f), 'utf8');
    const m = t.match(/<h1>([^<]+)<\/h1>/);
    if(m) slugMap[m[1]] = f.replace('.html', '');
  }

  // 기존 용어 enrichment 병합
  const enrichedExisting = existing.map(t => ({
    ...t,
    slug: slugMap[t.term] || t.term.toLowerCase().replace(/\s+/g, '-'),
    law_ref: (ENRICH[t.term] && ENRICH[t.term].law_ref) || '',
    example: (ENRICH[t.term] && ENRICH[t.term].example) || '',
    recent: (ENRICH[t.term] && ENRICH[t.term].recent) || null,
    updated: TODAY
  }));

  // 신규 용어 추가 (중복 방지: existing + NEW_TERMS 내부 dedup)
  const existingTerms = new Set(existing.map(t => t.term));
  const seenTerms = new Set(existingTerms);
  const seenSlugs = new Set(enrichedExisting.map(t => t.slug));
  const newUnique = [];
  for(const t of NEW_TERMS){
    if(seenTerms.has(t.term)) continue;
    if(seenSlugs.has(t.slug)){
      console.warn(`⚠ slug 중복 스킵: ${t.term} (${t.slug})`);
      continue;
    }
    seenTerms.add(t.term);
    seenSlugs.add(t.slug);
    newUnique.push(t);
  }

  const allTerms = [...enrichedExisting, ...newUnique];

  // slug 전체 맵 생성 (related 링크용)
  const allSlugs = {};
  for(const t of allTerms) allSlugs[t.term] = t.slug;

  // terms.json 업데이트
  const newJson = {
    version: '2.0',
    lastUpdated: TODAY,
    count: allTerms.length,
    terms: allTerms.map(t => {
      const base = {
        term: t.term,
        category: t.category,
        definition: t.definition,
        law_ref: t.law_ref || '',
        example: t.example || '',
        related: t.related || [],
        updated: t.updated || TODAY
      };
      return base;
    })
  };
  fs.writeFileSync(path.join(TERMS_DIR, 'terms.json'), JSON.stringify(newJson, null, 2), 'utf8');
  console.log(`✅ terms.json 업데이트: ${allTerms.length}개`);

  // HTML 일괄 생성
  let generated = 0;
  for(const t of allTerms){
    const html = generateHTML(t, allSlugs);
    fs.writeFileSync(path.join(TERMS_DIR, `${t.slug}.html`), html, 'utf8');
    generated++;
  }
  console.log(`✅ HTML 생성: ${generated}개`);

  // 카테고리별 집계
  const counts = {};
  for(const t of allTerms) counts[t.category] = (counts[t.category] || 0) + 1;
  console.log('📊 카테고리별:', counts);
}

main();
